const getRelativeTimestamp = (hoursAgo: number, minutesOffset: number = 0): string => {
  const d = new Date();
  d.setHours(d.getHours() - hoursAgo);
  d.setMinutes(d.getMinutes() - minutesOffset);
  return d.toISOString();
};

